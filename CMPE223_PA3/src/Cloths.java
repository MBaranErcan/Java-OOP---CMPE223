public class Cloths {
    private int ID;
    private String name;
    private int piece;

    public Cloths(int ID, String name, int piece) {
        this.ID = ID;
        this.name = name;
        this.piece = piece;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPiece() {
        return piece;
    }

    public void setPiece(int piece) {
        this.piece = piece;
    }
    
    
}
